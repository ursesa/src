#ifndef LOGA_LCD_H
#define LOGA_LCD_H


//==================================================================================================
// I N C L U D E S
//==================================================================================================

#include <stdbool.h>
#include <stdint.h>
#include "globals.h"
#include "LogA_PortsLib.h"
#include "../me_lib/meGPIO_Init.h"
#include "../me_lib/delay.h"


//==================================================================================================
// G L O B A L   T Y P E S
//==================================================================================================

//! I/O direction
typedef enum
{
	LCD_EntryMode_IncrementShiftRight = 0,	//!< Increment address or shift right
	LCD_EntryMode_DecrementShiftLeft,		//!< Decrement address or shift left
}
LCD_EntryMode_T;


//==================================================================================================
// G L O B A L   F U N C T I O N   D E C L A R A T I O N
//==================================================================================================

//--------------------------------------------------------------------------------------------------
// LCD_Initialize
//--------------------------------------------------------------------------------------------------
//! \brief     Initializes the LCD module.
//--------------------------------------------------------------------------------------------------
void LCD_Initialize(void);

//--------------------------------------------------------------------------------------------------
// LCD_Cleanup
//--------------------------------------------------------------------------------------------------
//! \brief     Cleans up the LCD module.
//--------------------------------------------------------------------------------------------------
void LCD_Cleanup(void);

//--------------------------------------------------------------------------------------------------
// LCD_On
//--------------------------------------------------------------------------------------------------
//! \brief     Switches LCD module on, according to given options.
//--------------------------------------------------------------------------------------------------
void LCD_On(void);

//--------------------------------------------------------------------------------------------------
// LCD_Off
//--------------------------------------------------------------------------------------------------
//! \brief     Switches LCD module off.
//--------------------------------------------------------------------------------------------------
void LCD_Off(void);

//--------------------------------------------------------------------------------------------------
// LCD_SetEntryMode
//--------------------------------------------------------------------------------------------------
//! \brief     Sets entry mode, according to given options.
//--------------------------------------------------------------------------------------------------
void LCD_SetEntryMode(LCD_EntryMode_T entryMode);

//--------------------------------------------------------------------------------------------------
// LCD_Clear
//--------------------------------------------------------------------------------------------------
//! \brief     Clears the LCD.
//--------------------------------------------------------------------------------------------------
void LCD_Clear(void);

//--------------------------------------------------------------------------------------------------
// LCD_WriteChar
//--------------------------------------------------------------------------------------------------
//! \brief     Writes character to specified position.
//--------------------------------------------------------------------------------------------------
void LCD_WriteChar(int line, int pos, char c);

//--------------------------------------------------------------------------------------------------
// LCD_WriteString
//--------------------------------------------------------------------------------------------------
//! \brief     Writes string to specified position.
//--------------------------------------------------------------------------------------------------
void LCD_WriteString(int line, int pos, const char* s);


#endif // LOGA_LCD_H

