/** @brief      Delay for us
 *  @details    Waiting for the specified us Time
*/
inline void delayUS(unsigned int DelayUS)
{
    volatile unsigned int count;
    for (count=0; count<DelayUS; count++)
    {
        delay();
    }
}
/*==============================================================================*/
/** @brief      Delay for ms
 *  @details    Waiting for the specified Time
*/
void delayMS(unsigned int DelayMS)
{
    unsigned int count;
    for (count=0; count<DelayMS; count++)
    {
        delayUS(1000);
    }
}

